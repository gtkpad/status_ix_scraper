def request():
    url = "https://status.ix.br"
    return requests.get(url, headers={'User-Agent': random.choice(USER_AGENT_LIST)})


def parse_result(html):
    bs = BeautifulSoup(html, 'html.parser')
    ix_list = bs.find_all("ul", {"class": "list-group components"})

    data = {}
    for ix in ix_list:
        name = ix.find("strong").text.strip()
        items = ix.find("div", {"class": "group-items"}).find_all('li', {'class': 'list-group-item'})
        ix_data = {}
        for item in items:
            status_div = item.find("div", {"class": "pull-right"})
            status = status_div.text.strip()
            status_div.decompose()
            item_name = item.text.strip()
            ix_data[item_name] = status
        data[name] = ix_data
    return data


if __name__ == '__main__':
    response = request()

    if response.status_code != 200:
        print({"error": "request error"})
        exit()
    print(parse_result(response.text))
